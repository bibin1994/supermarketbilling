package org.bibin.bean;

public class FinalList {
	
	
	private String fid;
	private String fname;
	private double fprice;
	private int fquantity;
	private int fdiscount;
	private double finaldiscount;
	private double finalamount;
	public String getFid() {
		return fid;
	}
	public String getFname() {
		return fname;
	}
	public double getFprice() {
		return fprice;
	}
	public int getFquantity() {
		return fquantity;
	}
	public int getFdiscount() {
		return fdiscount;
	}
	public double getFinaldiscount() {
		return finaldiscount;
	}
	public double getFinalamount() {
		return finalamount;
	}
	public void setFid(String fid) {
		this.fid = fid;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public void setFprice(double fprice) {
		this.fprice = fprice;
	}
	public void setFquantity(int fquantity) {
		this.fquantity = fquantity;
	}
	public void setFdiscount(int fdiscount) {
		this.fdiscount = fdiscount;
	}
	public void setFinaldiscount(double finaldiscount) {
		this.finaldiscount = finaldiscount;
	}
	public void setFinalamount(double finalamount) {
		this.finalamount = finalamount;
	}
	
	
	
	

}
